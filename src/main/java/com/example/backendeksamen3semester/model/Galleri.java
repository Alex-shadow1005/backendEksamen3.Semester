package com.example.backendeksamen3semester.model;

import lombok.AllArgsConstructor;
import lombok.Builder;
import lombok.Data;
import lombok.NoArgsConstructor;

import javax.persistence.*;

@Entity
@Data
@Builder
@NoArgsConstructor
@AllArgsConstructor
public class Galleri {

    @Id
    @GeneratedValue(strategy= GenerationType.IDENTITY)
    @Column(name = "galleriid", updatable = false, nullable = false)
    private int galleriId;

    @Column(name = "name")
    private String name;

    @Column(name = "type")
    private String type;

    @Column(name = "image", unique = false, nullable = false, length = 100000)
    private byte[] image;



    public int getGalleriId() {
        return galleriId;
    }

    public void setGalleriId(int galleriId) {
        this.galleriId = galleriId;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public byte[] getImage() {
        return image;
    }

    public void setImage(byte[] image) {
        this.image = image;
    }
}
